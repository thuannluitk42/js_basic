// Polyfill?
/**
 * Trong JavaScript, polyfill là một đoạn mã (thường là JavaScript thuần) được sử dụng để thêm các tính năng mới của JavaScript 
 * (hoặc Web API) vào các trình duyệt cũ không hỗ trợ những tính năng đó.
 * 🔧 Polyfill là gì?
    Polyfill mô phỏng lại hành vi của một tính năng mà trình duyệt không hỗ trợ.
    Thường được dùng để giúp ứng dụng chạy ổn định trên nhiều trình duyệt, 
    đặc biệt là các phiên bản cũ của Internet Explorer, Safari, v.v.
    📌 Khi nào cần dùng polyfill?
    Khi bạn muốn hỗ trợ các trình duyệt cũ nhưng vẫn muốn viết mã hiện đại (ES6+).
 */