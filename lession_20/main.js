/**
 * 1️⃣ BEM là gì? BEM (Block – Element – Modifier) là một quy tắc đặt tên giúp:
    Tổ chức CSS rõ ràng, dễ đọc
    Tránh xung đột tên class
    Dễ tái sử dụng và bảo trì
 */

/**
 * 2️⃣ Các thành phần chính của BEM
Thành phần	Ý nghĩa	                                        Cách đặt tên
Block	    Thành phần chính / độc lập	                    .block
Element	    Thành phần con của block	                    .block__element
Modifier	Biến thể của block hoặc element (trạng thái)	.block--modifier hoặc .block__element--modifier
 */

/**
 * 5️⃣ Tóm tắt quy ước
    Block: block
    Element: block__element
    Modifier: block--modifier hoặc block__element--modifier
 */
